<h1>What's the A11y toolkit?</h1>
<p>
	Since 2019 the organisation WebAIM (Web Accessibility In Mind) conducts an yearly accessibility
	evaluation of the homepages for the top 1,000,000 web sites.
</p>
<p>
	Their <Link href="https://webaim.org/projects/million/" target="_blank">WebAim Million study</Link
	> has shown that 95.9% of home pages in 2024 had accessibility failures. Most of those failures are
	really simple to fix and to prevent.
</p>
<h2>We want to fix this</h2>
<p>
	The A11y toolkit shows you techniques and best practices how to avoid the most common
	accessibility failures.
</p>

